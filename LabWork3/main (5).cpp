void очиститиВвід()
{
    cin.clear();
    cin.ignore(std::numeric_limits<std::streamsize>::max(), '\n');
}

void меню()
{
    cout << "================= МЕНЮ =================\n";
    cout << "1 - Завдання If10 (цілі числа A і B)\n";
    cout << "2 - Завдання If31 (обчислення g(x))\n";
    cout << "3 - Геометричне завдання (варіант 11)\n";
    cout << "0 - Вихід із програми\n";
    cout << "----------------------------------------\n";
    cout << "Ваш вибір: ";
}

int main()
{
    int вибір;

    do
    {
        меню();
        cin >> вибір;

        if (!cin)
        {
            cout << "Помилка вводу! Спробуйте ще раз.\n\n";
            очиститиВвід();
            continue;
        }

        switch (вибір)
        {
        case 1:
            завдання_If10();
            break;
        case 2:
            завдання_If31();
            break;
        case 3:
            завдання_Геометрія11();
            break;
        case 0:
            cout << "Програму завершено.\n";
            break;
        default:
            cout << "Такого пункту меню не існує.\n";
        }

        cout << "\n";
    } while (вибір != 0);

    return 0;
}

// ------------------------------------------------------------
// If10. Дано два цілі числа A і B.
// Якщо їх значення не рівні – присвоїти кожній змінній суму цих чисел,
// якщо рівні – присвоїти нуль.
// ------------------------------------------------------------
void завдання_If10()
{
    cout << "\n************* Завдання If10 *************\n";
    long long A, B;

    cout << "Введіть цілі числа A та B: ";
    cin >> A >> B;

    if (!cin)
    {
        cout << "Некоректне введення!\n";
        очиститиВвід();
        return;
    }

    if (A != B)
    {
        long long сума = A + B;
        A = сума;
        B = сума;
    }
    else
    {
        A = 0;
        B = 0;
    }

    cout << "Нові значення: A = " << A << ", B = " << B << endl;
}

// ------------------------------------------------------------
// If31. Для заданого дійсного x знайти значення функції g(x):
//
// g(x) = x + 1,   якщо x < -1
// g(x) = x^2 - 1, якщо -1 <= x <= 1
// g(x) = 3,       якщо x > 1
//
// Результат цілий (int).
// ------------------------------------------------------------
void завдання_If31()
{
    cout << "\n************* Завдання If31 *************\n";
    double x;
    cout << "Введіть значення x: ";
    cin >> x;

    if (!cin)
    {
        cout << "Некоректне введення!\n";
        очиститиВвід();
        return;
    }

    int g;

    if (x < -1.0)
        g = static_cast<int>(x + 1.0);
    else if (x <= 1.0)
        g = static_cast<int>(x * x - 1.0);
    else
        g = 3;

    cout << "Результат: g(x) = " << g << endl;
}

// ------------------------------------------------------------
// Геометричне завдання (варіант 11).
// Перевірити, чи належить точка (x, y) зафарбованій області,
// утвореній двома чвертями кіл радіуса r.
// ------------------------------------------------------------
void завдання_Геометрія11()
{
    cout << "\n************* Геометричне завдання (варіант 11) *************\n";

    double r;
    cout << "Введіть радіус r (>0): ";
    cin >> r;

    if (!cin || r <= 0.0)
    {
        cout << "Некоректний радіус!\n";
        очиститиВвід();
        return;
    }

    double x, y;
    cout << "Введіть координати точки (x y): ";
    cin >> x >> y;

    if (!cin)
    {
        cout << "Некоректні координати!\n";
        очиститиВвід();
        return;
    }

    // Верхнє коло з центром (0, r)
    bool верхняЧверть =
        (x >= 0.0) && (y >= 0.0) &&
        (x * x + (y - r) * (y - r) <= r * r + 1e-9);

    // Нижнє коло з центром (-r, -r)
    bool нижняЧверть =
        (x <= 0.0) && (y <= 0.0) &&
        ((x + r) * (x + r) + (y + r) * (y + r) <= r * r + 1e-9);

    bool належить = верхняЧверть || нижняЧверть;

    if (належить)
        cout << "Точка (" << x << ", " << y << ") НАЛЕЖИТЬ зафарбованій області.\n";
    else
        cout << "Точка (" << x << ", " << y << ") НЕ належить зафарбованій області.\n";
}
